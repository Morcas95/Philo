#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sol;
	int	i;
	int	sign;

	sol = 0;
	sign = 1;
	i = 0;
	while (nptr[i] == ' ' || nptr[i] == '\t' || nptr[i] == '\n'
		|| nptr[i] == '\v' || nptr[i] == '\f' || nptr[i] == '\r')
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		sol = sol * 10 + (nptr[i] - '0');
		i++;
	}
	return (sol * sign);
}
